# Memeriksa detail informasi dari image MongoDB versi terbaru
docker image inspect mongo:latest

# Memeriksa detail informasi dari container bernama 'contohmongo'
docker container inspect contohmongo

# Memeriksa detail informasi dari volume bernama 'mongodatavolume'
docker volume inspect mongodatavolume

# Memeriksa detail informasi dari network bernama 'mongonetwork'
docker network inspect mongonetwork